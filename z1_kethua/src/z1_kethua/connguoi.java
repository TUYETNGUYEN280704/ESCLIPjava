package z1_kethua;


public class connguoi {
	private String an;
	private String ngủ;
	public connguoi(String an, String ngủ) {
 		this.an = an;
		this.ngủ = ngủ;
	}
	public String getAn() {
		return an;
	}
	public void setAn(String an) {
		this.an = an;
	}
	public String getNgủ() {
		return ngủ;
	}
	public void setNgủ(String ngủ) {
		this.ngủ = ngủ;
	}
 
	
	public String anan() {
		return this.an;
	}
	
	public String uonguong() {
		return this.ngủ;
	}
	
}
 